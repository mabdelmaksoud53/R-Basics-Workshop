# R Basics Workshop Summary
# -------------------------------------------

# Purpose:
# This workshop is designed for complete beginners from any field
# to learn R programming and apply it to basic data analysis and
# descriptive statistics.

# Topics Covered:
# - Installing and setting up R and RStudio
# - Basic R syntax and common data structures
# - Importing and cleaning datasets
# - Descriptive statistics:
#     * Mean, median, mode
#     * Variance and standard deviation
#     * Frequency tables
# - Data visualization using:
#     * Base R (histograms, boxplots, bar charts)
#     * ggplot2 (grammar of graphics)
# - Introduction to dplyr for data manipulation

# Intended Audience:
# - Students and researchers from all disciplines
# - Individuals new to programming or data analysis
# - Anyone interested in learning statistics through R

